namespace Shimmer
{
    /// <summary>
    /// Represents the direction of the shimmering animation
    /// </summary>
    public enum ShimmeringDirection
    {
        /// <summary>
        /// Shimmer animation goes from left to right
        /// </summary>
        Right,

        /// <summary>
        /// Shimmer animation goes from right to left
        /// </summary>
        Left,

        /// <summary>
        /// Shimmer animation goes from below to above
        /// </summary>
        Up,

        /// <summary>
        /// Shimmer animation goes from above to below
        /// </summary>
        Down,
    }
}
